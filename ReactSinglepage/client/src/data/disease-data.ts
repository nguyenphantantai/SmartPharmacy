  // Tìm kiếm bệnh
  static searchDiseases(query: string): DiseaseItem[] {
    const allDiseases = [
      ...this.getPopularDiseases(),
      ...this.getSeasonalDiseases(),
    ];
    
    if (!query.trim()) {
      return allDiseases;
    }

    return allDiseases.filter(disease =>
      disease.label.toLowerCase().includes(query.toLowerCase())
    );
  }

